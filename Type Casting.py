"""conversion of one datat type to another is called as typecasting.

there are two types of type casting:

Implicit Type Conversion : where Python self converts one datatype to another.

Explicit Type Convesion : where the user converts one datatype to another."""

name = "jameel"
print(type(name))

a = 5;
b = 4.5;
print(type(a))
print(type(b))  
